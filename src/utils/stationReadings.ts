/** カタカナ → ひらがな 変換 */
export function normalizeToHiragana(str: string): string {
  return str
    .replace(/[\u30A1-\u30F6]/g, ch => String.fromCharCode(ch.charCodeAt(0) - 0x60))
    .replace(/ヶ/g, 'が')
    .replace(/ヵ/g, 'か')
    .toLowerCase();
}

