// ひらがな・カタカナ判定用の正規表現
const kanaOnlyRegex = /^[\u3040-\u309F\u30A0-\u30FF\u30FC\s・ー]+$/;

/**
 * 駅名のふりがなを返す
 * 漢字を含む駅名のみふりがなを返し、ひらがな/カタカナのみの駅名は空文字を返す
 */
export const getFurigana = (stationName: string): string => {
  // ひらがな・カタカナのみの駅名はふりがな不要
  if (kanaOnlyRegex.test(stationName)) {
    return '';
  }
  return furiganaMap[stationName] || '';
};
